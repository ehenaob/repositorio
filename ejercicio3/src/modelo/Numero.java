package modelo;

public class Numero
{
	private int numero;
	
	public Numero(int pNumero)
	{
		numero = pNumero;
	}

	public int darNumero() 
	                                                                                                                   {
		return numero;
	}

	public void cambiarNumero(int pNumero)
	{
		numero = pNumero;
	}
}
